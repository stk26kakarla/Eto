        {
            if (ModelState.IsValid)
            {
                User NewUser = FNHRepository.SaveOrUpdate<User>(new User { EmailId = model.Email, SecondaryEmailId = model.SecondaryEmail });

                var user = new ApplicationUser { UserName = model.Email, Email = model.Email, EtoUserId = NewUser.Id };
                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    ViewBag.SuccessMessage = model.Email + " Registered";
                    model = new MVC.Models.RegisterViewModel();
                    return View(model);
                    //var code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    //var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    //await UserManager.SendEmailAsync(user.Id, "Confirm your account", "Please confirm your account by clicking this link: <a href=\"" + callbackUrl + "\">link</a>");
                    //ViewBag.Link = callbackUrl;
                    //return View("DisplayEmail");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            //return View(model);
            return RedirectToAction("Index");

        }

        private void AddErrors(IdentityResult result)
        {
            foreach (var error in result.Errors)
            {
                ModelState.AddModelError("", error);
            }
        }

